disposición del Consumidor el valor a pagar por la gestión del encargo
solicitado, consistente en la compra de los productos seleccionados.<br>

&bull; Pago. El USUARIO realizará el pago directamente al ALIADO bien sea
en efectivo o a través de las diferentes formas de pago con las que
cuente el ALIADO o el EMPRESARIO.<br>

&bull; Forma de pago. El USUARIO debe seleccionar el medio de pago que
desea utilizar, teniendo como posibilidades el pago después del
consumo a través de dinero en efectivo o por medio de pago
electrónico a través de la plataforma de pago, disponible en la
aplicación.<br>

&bull; Registro. El USUARIO debe crear su cuenta personal en la cual se
requerirán los datos personales que permitan su identificación, mas no
su individualización, además de los datos para la realización del pago
en caso de pagar por tarjeta de crédito o débito.<br>

&bull; Resumen y Correo electrónico. Una vez completados los pasos
anteriores se le exhibe a través de una ventana emergente al
USUARIO un resumen detallado de la transacción y la información
completa del ALIADO que ha aceptado gestionar el encargo solicitado
y con el cual se celebra el contrato de mandato. Dicho resumen será
enviado por correo electrónico al USUARIO con ésta información de
forma detallada.<br>

&bull; Resumen del encargo. En todo caso, sea que el pago se realice en
dinero en efectivo o a través de la pasarela de pagos, se enviará vía
correo electrónico el resumen del encargo solicitado, con el detalle de
